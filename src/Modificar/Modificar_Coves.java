    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean modi=false;
        if(text_orga.getText().length()!=0 && text_pobla.getText().length()!=0){//valida los campos vacios
        modi=tcoves.modificarCoves(new Coves (text_rut_coves.getText(),text_orga.getText(),combo_estado.getItemAt(combo_estado.getSelectedIndex()),text_unidad.getText(),text_ano_creacion.getText(),text_pobla.getText(),text_telefono.getText()));
        if(modi){ 
                JOptionPane.showMessageDialog(null,"REGISTRO MODIFICADO",
                "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);
               Ver_Coves ver_coves=new Ver_Coves();
                ver_coves.ver_coves(text_rut_coves.getText(),9,Integer.parseInt(text_control.getText()));
                dispose();
        }
        else     JOptionPane.showMessageDialog(null,"REGISTRO PERDIDO",
                 "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);        
        }
        else{
            JOptionPane.showMessageDialog(null,"FALTAN CAMPOS POR LLENAR",
            "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);
        }
                    
    }//GEN-LAST:event_jButton1ActionPerformed

