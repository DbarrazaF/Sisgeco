    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(table_sectores.getSelectedRow()>=0) {
        if(Modificar_Sector.ventana_modificar_sector==false){
        Modificar_Sector modificar_sector=new Modificar_Sector();
        String cod_sector=String.valueOf(table_sectores.getValueAt(table_sectores.getSelectedRow(),0));
         String nom_sector=String.valueOf(table_sectores.getValueAt(table_sectores.getSelectedRow(),1));
         modificar_sector.modificar_sector(cod_sector,nom_sector);
         Modificar_Sector.ventana_modificar_sector=true;
         this.setVisible(false);
            }
            else{
                 JOptionPane.showMessageDialog(null,"VENTANA MODIFICAR SECTOR SE ENCUENTRA ABIERTA", 
            "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"PORFAVOR SELECCIONE UN SECTOR",
            "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);
        }
          
    }//GEN-LAST:event_jButton2ActionPerformed

