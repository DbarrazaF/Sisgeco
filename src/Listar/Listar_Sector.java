    private void Listar_Sector(){
     
        DefaultTableModel modelo= (DefaultTableModel) table_sectores.getModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        for(Sector sector : tsector.listarSector()){
           Object rowData[]={sector.getCod_sector(),sector.getNom_sector()};
           ((DefaultTableModel)table_sectores.getModel()).addRow(rowData);
           }
        
    
    }

