    private void Listar_Sector(){
        
        DefaultTableModel modelo= (DefaultTableModel) table_tipo_proyecto.getModel();
        modelo.addColumn("Numero");
        modelo.addColumn("Nombre");
        for(Tipo_Proyecto tipo_ptoyecto : ttipo_proyecto.listarTipoProyecto()){
           Object rowData[]={tipo_ptoyecto.getNro_tipo_proyecto(),tipo_ptoyecto.getNombre_tipo_proyecto()};
           ((DefaultTableModel)table_tipo_proyecto.getModel()).addRow(rowData);
           }
    }

