    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         //valida si se seleciono alguna fila de la tabla
        if(table_tipo_proyecto.getSelectedRow()>=0) {
             //verifica si la ventana se encuentra abierta
            if(Modificar_Tipo_Proyecto.ventana_modificar_tipo_proyecto==false){
        Modificar_Tipo_Proyecto modificar_tipo_proyecto=new Modificar_Tipo_Proyecto();
        String nro_tipo_pro=String.valueOf(table_tipo_proyecto.getValueAt(table_tipo_proyecto.getSelectedRow(),0));
          String nom__tipo_pro=String.valueOf(table_tipo_proyecto.getValueAt(table_tipo_proyecto.getSelectedRow(),1));
        modificar_tipo_proyecto.modificar_tipo_proyecto(nro_tipo_pro,nom__tipo_pro);
         Modificar_Tipo_Proyecto.ventana_modificar_tipo_proyecto=true;//cambia el valor de la variable en la siguente ventana
        dispose();
            }
            else{
                JOptionPane.showMessageDialog(null,"VENTANA MODIFICAR TIPO PROYECTO SE ENCUENTRA ABIERTA",
            "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);
            }
            }
        else{
            JOptionPane.showMessageDialog(null,"PORFAVOR SELECCIONE UN TIPO DE PROYECTO",
            "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

