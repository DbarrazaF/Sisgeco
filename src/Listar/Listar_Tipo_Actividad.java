    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       //valida si se seleciono alguna fila dela tabla
        if(table_lista_suceso.getSelectedRow()>=0) {
            //verifica si la ventana se encuentra abierta
           if(Modificar_Tipo_Actividad.ventana_modificar_tipo_suceso==false){
        Modificar_Tipo_Actividad modificar_tipo_suceso=new Modificar_Tipo_Actividad();
        String nro_tipo_suc=String.valueOf(table_lista_suceso.getValueAt(table_lista_suceso.getSelectedRow(),0));
          String nom_tipo_suc=String.valueOf(table_lista_suceso.getValueAt(table_lista_suceso.getSelectedRow(),1));
       modificar_tipo_suceso.modificar_tipo_proyecto(nro_tipo_suc, nom_tipo_suc);
       Modificar_Tipo_Actividad.ventana_modificar_tipo_suceso=true;//cambia el valor de la variable en la siguente ventana
        dispose();
           }
           else{
               JOptionPane.showMessageDialog(null,"VENTANA MODIFICAR TIPO SUCESO SE ENCUENTRA ABIERTA",
            "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);
           }
           }
       else{
           JOptionPane.showMessageDialog(null,"PORFAVOR SELECCIONE UN TIPO DE SUCESO",
            "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);
       }
        
    }//GEN-LAST:event_jButton2ActionPerformed

