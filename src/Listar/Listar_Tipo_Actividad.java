    private void Listar_Tipo_Actividad(){
        DefaultTableModel modelo= (DefaultTableModel) table_lista_suceso.getModel();
        modelo.addColumn("Codigo");
        modelo.addColumn("Nombre");
        for(Tipo_Actividad tipo_suceso : ttipo_suceso.listarTipo_Actividad()){
           Object rowData[]={tipo_suceso.getCod_tipo_suceso(),tipo_suceso.getNombre_tipo_suceso()};
           ((DefaultTableModel)table_lista_suceso.getModel()).addRow(rowData);
           }
    }

