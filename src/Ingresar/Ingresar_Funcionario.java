    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean ins = false;
        //validamos que los campos no estan vacios
       
        if (textrut_funci.getText().length() != 0 && textnom.getText().length() != 0 && textapp.getText().length() != 0 && textapm.getText().length() != 0 && textcontra.getText().length() != 0) {
            try {
                ins = tfuncio.insertarFuncionario(new Funcionario(textrut_funci.getText(), textnom.getText(), textapp.getText(), textapm.getText(), validar.encrypt(textcontra.getText()), tipocombo.getItemAt(tipocombo.getSelectedIndex()), text_estado.getText()));
                if (ins) {
                    JOptionPane.showMessageDialog(null, "REGISTRO INGRESADO A BD",
                            "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);
                    //reiniciamos los campos
                    textrut_funci.setText(null);
                    textnom.setText(null);
                    textapp.setText(null);
                    textapm.setText(null);
                    textcontra.setText(null);
                    ;
                } else {
                    JOptionPane.showMessageDialog(null, "REGISTRO PERDIDO O EXISTE",
                            "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);
                }
            } catch (Exception ex) {
                Logger.getLogger(Ingresar_Funcionario.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "FALTAN DATOS POR INGRESAR",
                    "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

