    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // se validan los campos
        if(check_retira.isSelected()==true && text_nro_decreto.getText().length()!=0 && date_fecha_decreto.getDate()!=null){
                String fecha_decreto= new SimpleDateFormat("yyyy-MM-dd").format(date_fecha_decreto.getDate());

                boolean ins=false;
                ins=tdecreto.insertarDecreto(new Decreto(text_nro_decreto.getText(),fecha_decreto,"0001-01-01","",
                ano_postu_proye.getText(),nro_ingre_proye.getText(),text_estado.getText()));
                 if(ins){ JOptionPane.showMessageDialog(null,"REGISTRO INGRESADO A BD",
                "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);

                Ver_Proyecto ver_proyecto=new Ver_Proyecto();
                ver_proyecto.ver_proyecto(ano_postu_proye.getText(),nro_ingre_proye.getText(),9, Integer.parseInt(text_control.getText()),text_cov.getText());
                dispose();
                ;}
                 else     JOptionPane.showMessageDialog(null,"REGISTRO PERDIDO O EXISTE",
                            "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);
        }//fin if
        //se valida si el campo fecha_retiro no este vacio y valida los demas campos
        else if(date_fecha_retiro.getDate()!=null && text_nro_decreto.getText().length()!=0 && date_fecha_decreto.getDate()!=null && text_rut_retira.getText().length()!=0){
              
            String fecha_decreto= new SimpleDateFormat("yyyy-MM-dd").format(date_fecha_decreto.getDate());
            String fecha_retiro_cheque= new SimpleDateFormat("yyyy-MM-dd").format(date_fecha_retiro.getDate());
        
            boolean ins=false;
            ins=tdecreto.insertarDecreto(new Decreto(text_nro_decreto.getText(),fecha_decreto,fecha_retiro_cheque,text_rut_retira.getText(),
                ano_postu_proye.getText(),nro_ingre_proye.getText(),text_estado.getText()));
            if(ins){ JOptionPane.showMessageDialog(null,"REGISTRO INGRESADO A BD",
                         "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);

                Ver_Proyecto ver_proyecto=new Ver_Proyecto();
                ver_proyecto.ver_proyecto(ano_postu_proye.getText(),nro_ingre_proye.getText(),9, Integer.parseInt(text_control.getText()),text_cov.getText());
                dispose();
            }

                else     JOptionPane.showMessageDialog(null,"REGISTRO PERDIDO O EXISTE",
                "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);
        }//fin else if
        else{
             JOptionPane.showMessageDialog(null,"FALTAN DATOS POR INGRESAR",
            "AVISO DE SISTEMA", JOptionPane.PLAIN_MESSAGE);
        }//fin else
        
    }//GEN-LAST:event_jButton1ActionPerformed

